package oop1;

public class Test02DataType {
    public static void main(String[] args) {
        //기본자료

        //실수
        //float < double

        float f=1.2f; //접미사 f를 붙여야 함.
        double d=3.4d;//접미사 d를 붙여야 함. d는 생략 가능하다.

        System.out.println(f);
        System.out.println(d);
    }
}
